const pawnEvalBlack = pawnEvalWhite.slice().reverse();
var bishopEvalBlack = bishopEvalWhite.slice().reverse();
var rookEvalBlack = rookEvalWhite.slice().reverse();
var kingEvalBlack = kingEvalWhite.slice().reverse();

const getPieceValue = (params: {piece: Piece | null; x: number; y: number}) => {
  const {piece, x, y} = params;

  if (piece === null) {
    return 0;
  }

  let absoluteValue: number;

  const pieceType = piece.type;
  const isWhite = piece.color === 'w';

  switch (pieceType) {
    case 'p': {
      absoluteValue =
        10 + (isWhite ? pawnEvalWhite[y][x] : pawnEvalBlack[y][x]);
      break;
    }
    case 'r': {
      absoluteValue =
        50 + (isWhite ? rookEvalWhite[y][x] : rookEvalBlack[y][x]);
      break;
    }
    case 'n': {
      absoluteValue = 30 + knightEval[y][x];
      break;
    }
    case 'b': {
      absoluteValue =
        30 + (isWhite ? bishopEvalWhite[y][x] : bishopEvalBlack[y][x]);
      break;
    }
    case 'q': {
      absoluteValue = 90 + queenEval[y][x];
      break;
    }
    case 'k': {
      absoluteValue =
        900 + (isWhite ? kingEvalWhite[y][x] : kingEvalBlack[y][x]);
      break;
    }
    default: {
      throw 'Unknown piece type: ' + piece.type;
    }
  }

  return isWhite ? absoluteValue : -absoluteValue;
};

